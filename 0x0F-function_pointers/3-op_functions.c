#include "3-calc.h"
/**
 * op_add - calculate the sum of two integer
 * @a: 1st integer
 * @b: 2d integer
 * Return: sum of: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - calculate substraction of two integer
 * @a: 1st integer
 * @b: 2d integer
 * Return: sub of: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - calculate multiplication of two integer
 * @a: 1st integer
 * @b: 2d integer
 * Return: sub of: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - calculate division of two integer
 * @a: 1st integer
 * @b: 2d integer
 * Return: div of: a * b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - calculate remainder of the division of two integer
 * @a: 1st integer
 * @b: 2d integer
 * Return: remainder of the div : a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
